    public instructionStudent(String rollNo1) {
        initComponents();
        rollNo = rollNo1;
        jLabel_rollNo.setText(rollNo);
        byte[] image = null;
        jDateChooser_dob.setDateFormatString("dd-MM-yyyy");
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, 
                        ResultSet.CONCUR_UPDATABLE);//Notice, this line may be throw sql exception 
            ResultSet rs = st.executeQuery("select avatar, name, marks, password, dob, gender, contactNo, email, address from user where rollNo = '"+rollNo+"'");
            
            while(rs.next()){
                image = rs.getBytes(1);
                Image img = Toolkit.getDefaultToolkit().createImage(image).getScaledInstance(150, 150, Image.SCALE_SMOOTH);
                ImageIcon icon = new ImageIcon(img);
                jLabel_avatar.setIcon(icon);
                jLabel_name.setText(rs.getString(2));
                jTextField_Name.setText(rs.getString(2));
                jLabel_marks.setText(String.valueOf(rs.getInt(3)));
                jPasswordField_password.setText(rs.getString(4));
                String format_dob = rs.getString(5).substring(8) + "-"+rs.getString(5).substring(5,7)+"-"+rs.getString(5).substring(0,4);
                jDateChooser_dob.setDate(new SimpleDateFormat("dd-MM-yyyy").parse(format_dob));
                jComboBox_gender.setSelectedItem(rs.getString(6));
                jTextField_contact.setText(rs.getString(7));
                jTextField_email.setText(rs.getString(8));
                jTextField_address.setText(rs.getString(9));
            }
            jTextField_Name.setEnabled(false);
            jPasswordField_password.setEnabled(false);
            jDateChooser_dob.setEnabled(false);
            jComboBox_gender.setEnabled(false);
            jTextField_contact.setEnabled(false);
            jTextField_email.setEnabled(false);
            jTextField_address.setEnabled(false);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
