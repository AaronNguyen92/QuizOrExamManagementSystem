    private void jButton_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveActionPerformed
        // TODO add your handling code here:        
        String name = jTextField_Name.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String dob = sdf.format(jDateChooser_dob.getDate());
        String gender = String.valueOf(jComboBox_gender.getSelectedItem());
        String contactNo = jTextField_contact.getText();
        String email = jTextField_email.getText();
        String address = jTextField_address.getText();
        String password = jPasswordField_password.getText();
        ImageIcon icon_avatar = (ImageIcon) jLabel_avatar.getIcon();
        BufferedImage bi = new BufferedImage(icon_avatar.getIconWidth(), 
                    icon_avatar.getIconWidth(), BufferedImage.TYPE_INT_ARGB);
        Graphics2D bGr = bi.createGraphics();
        bGr.drawImage(icon_avatar.getImage(), 0, 0, null);
        bGr.dispose();
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        
        try {
            ImageIO.write(bi, "png", baos);
            byte[] byteArray = baos.toByteArray();
            
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("update user set name = ?,dob = ?,gender = ?, contactNo = ?,email = ?,address = ?,password = ?,avatar = ? where rollNo = ?");
            ps.setString(1, name);
            ps.setString(2, dob);
            ps.setString(3, gender);
            ps.setString(4, contactNo);
            ps.setString(5, email);
            ps.setString(6, address);
            ps.setString(7, password);
            ps.setBytes(8, byteArray);
            ps.setString(9, rollNo);
            ps.executeUpdate();
            JFrame frame = new JFrame();
            frame.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(frame, "Successfully Registered!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton_saveActionPerformed

